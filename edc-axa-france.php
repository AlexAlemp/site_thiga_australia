                                <p class="lead">
                                    La direction marketing d'AXA a sollicité Thiga pour l'aider dans la mise en place d'une cellule innovante ayant pour objectif de permettre aux différentes directions d'accélérer leurs projets digitaux.
                                </p>
                            
                                <p>
                                    En mettant en place cette nouvelle entité AXA souhaite avoir la capacité de valider le plus vite possible une solution en investissant très peu de temps et de budget.
                                    Construit autour de la <strong>méthodologie Design Sprint</strong>, l’Accélérateur AXA France aide les porteurs de projets métier qui souhaitent repenser certains de leurs parcours, répondre à une problématique utilisateur ou créer une nouvelle expérience innovante. 

                                </p>
                                <figure>
                                    <img alt="Prototypage Design Sprint" src="/img/edc/axa/edc-axa-min-4.jpg">
                                    <figcaption>Séance de prototypage</figcaption>
                                </figure>
                                <h2>Démarche initiale</h2>
                                <p>
                                    Adepte et expert de la méthodologie Design Thinking, nous avons partagé notre savoir faire avec les équipes d’AXA France afin de les aider à mettre en place l’Accélérateur.<br>
                                    Nous avons fait un état des lieux de l’existant afin de bien comprendre quelles étaient les parties prenantes de cette initiative, les objectifs pour les différentes directions, pour le groupe, ainsi que les outils à disposition. 
                                </p>
                                <blockquote>
                                    Nous nous sommes ainsi vite rendus compte que déployer le Design Sprint tel que présenté par Google Ventures ne serait certainement pas la meilleure formule.
                                </blockquote>
                                <p>
                                    En effet, la maturité de certains projets portés par le métier, l’exigence de qualité des prototypes exigée, ainsi que la difficulté à pouvoir réunir certains experts durant plus de 2 jours, nous a obligé à repenser la dynamique du Design Sprint.Nous avons donc construit un processus sur 10 jours - au lieu des 5 jours décrits dans le livre “Sprint” - bien plus adapté à notre contexte. </p>
                                <p>
                                    Ainsi en 2 semaines, l’équipe de l’Accélérateur (un sprint master, 2 UX/UI designers) accompagne les équipes métier en suivant les étapes suivantes :<br>
                                    - Deux jours d’ateliers d’intelligence collective permettant de comprendre la problématique et trouver la solution la plus pertinente. <br>
                                    - Trois jours dédiés à la création du prototype.<br>
                                    - Une première journée de tests utilisateurs suivie d’une journée de présentation des retours utilisateurs et des modifications à apporter au parcours utilisateur. <br>
                                    - Deux jours de “polish” du prototype <br>
                                    - Une dernière journée de tests utilisateurs conclue par une restitution globale du Design Sprint<br>

                                </p>
                                 <h2>Démarche mise à jour</h2>
                                <p>
                                    Cependant, à la suite des premiers sprints, nous avons identifié deux failles dans ce processus : <br>
                                    - Certaines problématiques métier n’étaient pas suffisamment mature pour pouvoir y répondre à l’aide d’un Design Sprint<br>
                                    - Une fois le prototype validé, il manquait une transition encadrée vers le développement d’un POC par les équipes techniques<br>
                                </p>
                                <p>
                                    Nous avons donc ajouté deux nouvelles démarches à notre Accélérateur :<br> 
                                    - Une phase amont appelée <strong>“learn and decide” </strong>destinée à aider le métier à connaître ses utilisateurs, leurs besoins, leurs points de douleur et ainsi identifier la / les problématique(s) qui peuvent ensuite faire l’objet d’un Design Sprint.<br>
                                    - Une phase post-Design Sprint nommée <strong>“Execute”</strong>qui est constituée d”d’une équipe technique full stack, totalement intégrée à l’Accélérateur, qui prend le relais de l’équipe Design Sprint lorsqu’un prototype a été validé et le budget accepté par la direction métier ; cela permet d’éviter de possibles pertes de cohérence entre ce qui a été prototypé en Design Sprint et ce qui sera ensuite mis en production
                                </p>
                                <p>
                                    Enfin, l’un des aspect important de la démarche est que la performance ne se mesure pas uniquement par le nombre de projets lancés suite à leur passage au sein de l’Accélérateur, mais aussi au nombre d’idées n’ayant pas été validées. En effet cela permet de ne pas investir de budget sur des projets ne répondant pas de manière pertinente à une problématique utilisateur ou métier.

                                </p>
                                <h2>Équipe</h2>
                                <p>
                                Lors de ce Design Sprint, notre expert Design Sprint a été sollicité et accompagne la direction marketing AXA France depuis de très nombreux mois.
                                </p>
                                <h2>Expertises</h2>
